-- ///// GetSellValue 		//////
-- ///////////////////////////////

function VehicleCosts:GetSellValue(id)
	local string = self.vehicleCosts[id];
	if(string) then
		
		local dollar, tokens = tonumber(gettok(string, 1, ",")), tonumber(gettok(string, 2, ","))
		
		if(dollar) then
			return dollar/100*50
		elseif(tokens) then
			return (tokens/100*50)*10
		end
	end
	
	return 0;
end

