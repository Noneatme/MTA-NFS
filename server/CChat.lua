	end
	return obj;
end


-- ///////////////////////////////
-- ///// SendText 			//////
-- ///////////////////////////////

function ChatBoxServer:SendText(group, text)
	local player = source;
	--text = string.gsub(getPlayerName(player), '#%x%x%x%x%x%x', '')..": "..text
	
	if(self:ContainsBadWords(text) == true) then
		text = self.randomSaetze[math.random(1, #self.randomSaetze)];
	end
	text = getPlayerName(player)..": "..text
		
	logger:OutputMySQL("Chat Message", "["..group.."]"..text);
		
	triggerClientEvent(getRootElement(), "ChatBox:SendTextBack", getRootElement(), group, text);

end

-- ///////////////////////////////
-- ///// SendCommand 		//////
-- ///////////////////////////////

function ChatBoxServer:SendCommand(cmd, params)
	local player = source;
	
	executeCommandHandler(cmd, player, unpack(params))
end


-- ///////////////////////////////
-- ///// ContainsBadWords   //////
-- ///////////////////////////////

function ChatBoxServer:ContainsBadWords(text)
	local new_val = string.lower(text);

	for index, val in pairs(self.insults) do
		if(string.find(new_val, string.lower(val), 0, true)) then
			return true;
		end
	end
	
	local last_byte;
	local warnings = 0;
	
	for i = 1, string.len(new_val), 1 do
		local newbyte = string.byte(new_val, i);
		if(newbyte == last_byte) then
			warnings = warnings+1;
		
			if(warnings == 5) then
				return true;
			end
		end
		last_byte = newbyte;
	end
	return false;
end

